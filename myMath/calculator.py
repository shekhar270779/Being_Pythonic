def addition(x, y):
    return x + y


def subtraction(x, y):
    return x - y


def multiplication(x, y):
    return x * y


def division(x, y):
    return x / y


if __name__ == "__main__":
    x = 10
    y = 4

    print(addition(x, y))
    print(subtraction(x, y))
    print(multiplication(x, y))
    print(division(x, y))